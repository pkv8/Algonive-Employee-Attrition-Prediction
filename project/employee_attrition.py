acc_log_bal = accuracy_score(y_test, y_pred_log_bal)
rec_log_bal = recall_score(y_test, y_pred_log_bal)

acc_rf = accuracy_score(y_test, y_pred_rf)
rec_rf = recall_score(y_test, y_pred_rf)

