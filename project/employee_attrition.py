import pandas as pd

df = pd.read_csv('WA_Fn-UseC_-HR-Employee-Attrition.csv')

df.head(10)

df.info()

pd.options.display.max_columns = None
df.head(10)

"""I convert categorical values like Yes/No and Male/Female into numerical form because machine learning models cannot process text directly. This step ensures that all input features are in a format suitable for model training."""

#Attrition, Gender, OverTime, Over18
df['Attrition'] = df['Attrition'].map({'Yes': 1, 'No': 0})
df['Gender'] = df['Gender'].map({'Male': 1, 'Female': 0})
df['OverTime'] = df['OverTime'].map({'Yes': 1, 'No': 0})
df['Over18'] = df['Over18'].map({'Y': 1, 'N': 0})

pd.options.display.max_columns = None
df.head(10)

pd.get_dummies(df['MaritalStatus'])

"""I convert categorical columns such as JobRole and Department into multiple binary columns using one-hot encoding. This avoids introducing incorrect numerical relationships and allows the model to treat each category independently.**bold text**"""

#remaining categorical One-Hot Encoding: BusinessTravel, Department, Education, jobrole, MaritalStatus
df = df.join(pd.get_dummies(df['BusinessTravel'], prefix='BusinessTravel')).drop('BusinessTravel', axis=1)
df = df.join(pd.get_dummies(df['Department'], prefix='Department')).drop('Department', axis=1)
df = df.join(pd.get_dummies(df['EducationField'], prefix='Education')).drop('EducationField', axis=1)
df = df.join(pd.get_dummies(df['JobRole'], prefix='JobRole')).drop('JobRole', axis=1)
df = df.join(pd.get_dummies(df['MaritalStatus'], prefix='MaritalStatus')).drop('MaritalStatus', axis=1)

pd.options.display.max_columns = None
df.head(10)

df.info()

"""I convert True and False values into 1 and 0 so that all data in the dataset is numerical. This ensures consistency and prevents errors during model training, as machine learning algorithms require numeric inputs.


"""

df = df.map(lambda x:1 if x is True else 0 if x is False else x)

"""I remove columns like EmployeeNumber and StandardHours because they do not contribute to predicting employee attrition. Keeping irrelevant features can reduce model performance and introduce unnecessary complexity.


"""

import matplotlib.pyplot as plt

df.hist(figsize=(20,20))
plt.show

df = df.drop(['EmployeeCount','EmployeeNumber','StandardHours','Over18'], axis=1)

