rec_dt_imp = recall_score(y_test, y_pred_dt_imp)

"""Finally, I compared all models using accuracy and recall. Since the dataset is imbalanced, recall for attrition (employees who leave) is more important than accuracy.

I created a comparison table and selected the best model based on recall performance.
"""

import pandas as pd

comparison = pd.DataFrame({
    "Model": [
        "Logistic Regression",
        "Improved Logistic Regression",
        "Random Forest",
        "Improved Random Forest",
        "Decision Tree",
        "Improved Decision Tree"
    ],
    "Accuracy": [
        acc_log, acc_log_bal, acc_rf, acc_rf_imp, acc_dt, acc_dt_imp
    ],
    "Recall (Attrition=1)": [
        rec_log, rec_log_bal, rec_rf, rec_rf_imp, rec_dt, rec_dt_imp
    ]
})

comparison = comparison.round(3)
print(comparison)

best_model = comparison.sort_values(by="Recall (Attrition=1)", ascending=False).iloc[0]

print("\nBEST MODEL\n")
print("Model Name:", best_model["Model"])
print("Accuracy:", best_model["Accuracy"])
print("Recall (Attrition=1):", best_model["Recall (Attrition=1)"])

